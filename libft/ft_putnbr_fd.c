#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int val;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	val = n;
	if (val >= 10)
	{
		ft_putnbr_fd(val / 10, fd);
	}
	ft_putchar_fd(val % 10 + '0', fd);
}
