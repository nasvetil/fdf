#include "libft.h"

void	ft_putnbr(int n)
{
	int val;

	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	val = n;
	if (val >= 10)
	{
		ft_putnbr(val / 10);
	}
	ft_putchar(val % 10 + '0');
}
