#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char *buff;

	buff = dst;
	while (*src != '\0')
	{
		*buff = *src;
		buff++;
		src++;
	}
	*buff = '\0';
	return (dst);
}
